#include<iostream>
#include "029_hw_class_separate1_Circle.h" //원래는 메인 함수를 묶을 때 헤더 파일이 중복참조가 되어서 오류 발생
using namespace std;

Circle::Circle() {
	radius = 1;
	cout << "반지름" << radius;
	cout << " 원 생성" << endl;
}
Circle::Circle(int r) {
	radius = r;
	cout << "반지름" << radius;
	cout << " 원 생성" << endl;
}
double Circle::getArea() {
	return 3.14 * radius * radius;
}
